/** Incremento (++) ou (var = var + 1) **/

/** Pré incremento - incrementa o número antes de qualquer execusão **/

var num = 5;
console.log(++num);




/** Pós incremento - incrementa o número após a primeira execusão **/

var num = 10;
console.log(num++);
console.log(num);




/** Decremento (--) ou (var = var - 1) **/
/** O decremento segue a mesma lógica de pré e pós processamneto do incremento **/

/** Pré decremento **/

var num = 5;
console.log(--num);




/** Pós decremento **/

var num = 10;
console.log(num--);
console.log(num);

