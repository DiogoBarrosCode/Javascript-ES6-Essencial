/*** Operadores Aritiméticos ***/

/** Adição (+) **/

var sum = 1 + 2;
console.log(sum);



/** Subtração (-) **/

var sub = 3 - 1;
console.log(sub);




/** Multiplicação (*) **/

var multi = 2 * 10;
console.log(multi);




/** Divisão (/) **/

var div = 50 / 2;
console.log(div);




/** Módulo (%) - resto da divisão de dois números **/

var resto = 8 % 3;
console.log(resto);



/** Potência (**)
 * 
 * exemplo: 5 ** 3 = 125 ou seja 5 * 5 * 5  = 125
 * 
 * **/

var potencia = 5 ** 3;
console.log(potencia);




