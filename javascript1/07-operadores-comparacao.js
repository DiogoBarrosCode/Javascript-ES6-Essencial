/*** Operadores de comparação ***/

/** Os operadores de comparação verificam se uma determinada
comparação entre dois valores é verdadeira ou falsa **/

/** Igualdade (==) - verifica se dois valores são iguais **/

var num1 = 10;
var num2 = num1;
console.log(num1 == num2);




/** Igualdade (===) - além de comparar o valor também compara o tipo de dados **/

var op1 = 10;
var op2 = "10";
console.log(op1 === op2);




/** Diferente de (!=) - compara se dois valores são diferentes **/

var numOne = 10;
var numTwo = 10;
console.log(numOne != numTwo);




/** Maior que (>) e Menor que (<) - Comparam se um valor é maior ou menor que outro **/

var valor1 = 5;
var valor2 = 7;
console.log(valor1 > valor2);
console.log(valor1 < valor2);




/** Maior ou igual (>=) e Menor ou igual (<=) - Comparam se um valor é maior ou igual e se é menor ou igual que outro **/

var value1 = 5;
var value2 = 7;
var value3 = value2;
console.log(value2 <= value3);
console.log(value1 >= value3);

