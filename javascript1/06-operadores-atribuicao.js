/*** Operadores de atribuição ***/

/** Atribuição (=) - Atribui um valor especificado a algum objeto **/

var num1 = 5;
var num2 = num1;
console.log(num2);




/** Atribuição de adição (+=) ou (x = x + y) **/

var x = 5;
var y = 7;
x += y;
console.log(x);




/** Atribuição de subtração (-=) ou (x = x - y) **/

var x = 5;
var y = 1;
x -= y;
console.log(x);




/** Atribuição de multiplicação (*=) ou (x = x * y) **/

var x = 5;
var y = 2;
x *= y;
console.log(x);




/** Atribuição de divisão (/=) ou (x = x / y) **/

var x = 5;
var y = 2;
x /= y;
console.log(x);




/** Atribuição de módulo (%=) ou (x = x % y) **/
var x = 7;
var y = 3;
x %= y;
console.log(x);


